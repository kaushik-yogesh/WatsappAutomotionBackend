// GET /api/partner/dashboard
exports.getPartnerDashboard = catchAsync(async (req, res, next) => {
  const user = req.user;

  // Ensure partner code exists
  if (!user.partnerCode) {
    user.partnerCode = await generateUniquePartnerCode();
    await user.save({ validateBeforeSave: false });
  }

  // Get referred users
  const referredUsers = await User.find({ referredByPartner: user._id })
    .select('name email createdAt subscription')
    .sort('-createdAt');

  // Auto-sync missing commissions strictly for captured payment records
  const Payment = require('../models/Payment');
  const settings = await getSystemSettings();
  const rate = user.partnerCommissionRate || settings.defaultPartnerCommissionRate || 20;

  for (const refUser of referredUsers) {
    const capturedPayments = await Payment.find({ user: refUser._id, status: 'captured' });
    for (const pmt of capturedPayments) {
      const existing = await PartnerCommission.findOne({
        partner: user._id,
        referredUser: refUser._id,
        $or: [
          { payment: pmt._id },
          { paymentAmount: (pmt.amount / 100), notes: { $regex: pmt.plan } }
        ]
      });
      if (!existing && pmt.amount > 0) {
        const priceInRupees = pmt.amount / 100;
        const commAmount = Math.round((priceInRupees * rate) / 100);

        if (commAmount > 0) {
          await PartnerCommission.create({
            partner: user._id,
            referredUser: refUser._id,
            payment: pmt._id,
            paymentAmount: priceInRupees,
            commissionAmount: commAmount,
            commissionRate: rate,
            status: 'APPROVED',
            notes: `Auto-synced referral commission for captured payment plan: ${pmt.plan}`
          });
        }
      }
    }
  }

  // Get commissions summary
  const commissions = await PartnerCommission.find({ partner: user._id });

  const totalEarned = commissions
    .filter(c => c.status === 'APPROVED' || c.status === 'PAID')
    .reduce((sum, c) => sum + c.commissionAmount, 0);

  const pendingPayout = commissions
    .filter(c => c.status === 'APPROVED')
    .reduce((sum, c) => sum + c.commissionAmount, 0);

  const paidOut = commissions
    .filter(c => c.status === 'PAID')
    .reduce((sum, c) => sum + c.commissionAmount, 0);

  res.status(200).json({
    status: 'success',
    data: {
      partnerCode: user.partnerCode,
      commissionRate: user.partnerCommissionRate || settings.defaultPartnerCommissionRate,
      commissionType: settings.commissionType,
      minPayoutThreshold: settings.minPayoutThreshold,
      totalReferrals: referredUsers.length,
      totalEarned,
      pendingPayout,
      paidOut,
      referredUsers,
      recentCommissions: commissions.slice(0, 10)
    }
  });
});

