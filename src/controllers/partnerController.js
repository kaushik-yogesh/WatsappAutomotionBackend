// GET /api/partner/admin/partners
exports.getAllPartners = catchAsync(async (req, res, next) => {
  const partners = await User.find({ role: 'sales_partner' })
    .select('name email role partnerCode partnerCommissionRate createdAt')
    .sort('-createdAt');

  const Payment = require('../models/Payment');

  // Populate total earnings and sales revenue for each partner
  const partnerData = await Promise.all(partners.map(async (p) => {
    const referredUsers = await User.find({ referredByPartner: p._id }).select('_id');
    const referredUserIds = referredUsers.map(u => u._id);

    // Calculate total revenue generated by referred users
    const capturedPayments = await Payment.find({ user: { $in: referredUserIds }, status: 'captured' });
    const totalRevenueGenerated = capturedPayments.reduce((sum, pmt) => sum + ((pmt.amount || 0) / 100), 0);

    const commissions = await PartnerCommission.find({ partner: p._id });
    
    const totalEarned = commissions
      .filter(c => c.status === 'APPROVED' || c.status === 'PAID')
      .reduce((sum, c) => sum + c.commissionAmount, 0);

    const pendingPayout = commissions
      .filter(c => c.status === 'APPROVED')
      .reduce((sum, c) => sum + c.commissionAmount, 0);

    const paidOut = commissions
      .filter(c => c.status === 'PAID')
      .reduce((sum, c) => sum + c.commissionAmount, 0);

    return {
      ...p.toObject(),
      referralsCount: referredUsers.length,
      totalRevenueGenerated,
      totalEarned,
      pendingPayout,
      paidOut
    };
  }));

  res.status(200).json({
    status: 'success',
    data: { partners: partnerData }
  });
});

// GET /api/partner/admin/partner-users/:partnerId
exports.getPartnerReferredUsers = catchAsync(async (req, res, next) => {
  const { partnerId } = req.params;

  const partner = await User.findById(partnerId).select('name email role partnerCode partnerCommissionRate createdAt');
  if (!partner) return next(new AppError('Sales Partner not found', 404));

  const Payment = require('../models/Payment');
  const referredUsers = await User.find({ referredByPartner: partnerId })
    .select('name email createdAt subscription role')
    .sort('-createdAt');

  const userSummaries = await Promise.all(referredUsers.map(async (refUser) => {
    // Get captured payments for this user
    const payments = await Payment.find({ user: refUser._id, status: 'captured' })
      .select('plan amount razorpayPaymentId createdAt billingPeriod')
      .sort('-createdAt');

    const totalPaidAmount = payments.reduce((sum, p) => sum + ((p.amount || 0) / 100), 0);

    // Get commissions generated by this user for the partner
    const commissions = await PartnerCommission.find({ partner: partnerId, referredUser: refUser._id })
      .sort('-createdAt');

    const totalCommissionEarned = commissions
      .filter(c => c.status === 'APPROVED' || c.status === 'PAID')
      .reduce((sum, c) => sum + c.commissionAmount, 0);

    return {
      ...refUser.toObject(),
      paymentsCount: payments.length,
      totalPaidAmount,
      totalCommissionEarned,
      payments,
      commissions
    };
  }));

  const totalRevenueGenerated = userSummaries.reduce((sum, u) => sum + u.totalPaidAmount, 0);
  const totalCommissionsEarned = userSummaries.reduce((sum, u) => sum + u.totalCommissionEarned, 0);

  res.status(200).json({
    status: 'success',
    data: {
      partner,
      totalReferredUsers: referredUsers.length,
      totalRevenueGenerated,
      totalCommissionsEarned,
      referredUsers: userSummaries
    }
  });
});

// POST /api/partner/admin/assign-role
exports.assignPartnerRole = catchAsync(async (req, res, next) => {
  const { userId, role, customCommissionRate } = req.body;

  const user = await User.findById(userId);
  if (!user) return next(new AppError('User not found', 404));

  if (role) user.role = role;
  if (customCommissionRate !== undefined) user.partnerCommissionRate = customCommissionRate;

  if (user.role === 'sales_partner' && !user.partnerCode) {
    user.partnerCode = await generateUniquePartnerCode();
  }

  await user.save({ validateBeforeSave: false });

  res.status(200).json({
    status: 'success',
    message: `User ${user.email} updated successfully.`,
    data: { user }
  });
});

